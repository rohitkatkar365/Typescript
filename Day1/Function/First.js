"use strict";
// Normal Function
function greet(a) {
    console.log(a);
}
greet("Hello Ram");
// Function with return type
function greet1(a) {
    return a;
}
console.log(greet1("Hello Krishna"));
// Arrow function
const app = (a) => {
    return a;
};
console.log(app("Hello"));
// Optional Parameter
function sample(a) {
    console.log(a);
}
sample(1);
